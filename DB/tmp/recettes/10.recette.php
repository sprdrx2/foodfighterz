- 1) Dans une grande casserole, faites cuire dans un grand volume d'eau salée les pâtes tricolores en les gardant bien fermes. Egouttez-les et placez la passoire sous le robinet d'eau fraîche. Brassez les pâtes sous l'eau jusqu'à elles aient bien refroidi (cette opération stoppe leur cuisson, les garde fermes, et les empêche de coller en les débarrassant de l'amidon de blé dissous dans l'eau de cuisson). Egouttez à nouveau.
